package com.example.rm0016nc.dictionary;

public class Dicts {
    private String Words;
    private String Defs;

    public Dicts(String words, String defs) {
        Words = words;
        Defs = defs;
    }

    public String getWords() {
        return Words;
    }

    public String getDefs() {
        return Defs;
    }
}
